using System;
using System.Drawing;

namespace Com.Suncor.Olt.Common.Utility
{
    [Serializable]
    public class LabelAttributes
    {
        private readonly Font font;
        private readonly float height;
        private readonly float width;

        public LabelAttributes(Font font, float width, float height)
        {
            this.font = font;
            this.width = width;
            this.height = height;
        }

        public float Height
        {
            get { return height; }
        }

        public float Width
        {
            get { return width; }
        }

        public Font Font
        {
            get { return font; }
        }
    }
}