    [TestFixture]
    public class AcidClothingTypeTest
    {
        [Test]
        public void ToStringShouldReturnAForAcidClothingTypeASoThatAcidClothingTypeDisplaysCorrectlyInEditHistory()
        {
            Assert.AreEqual("A", AcidClothingType.A_ACIDCLOTHINGTYPE.ToString());
