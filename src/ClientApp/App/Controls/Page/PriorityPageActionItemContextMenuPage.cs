using System.Collections.Generic;
using Com.Suncor.Olt.Common.DTO;

namespace Com.Suncor.Olt.Client.Controls.Page
{
    public class PriorityPageActionItemContextMenuPage : ActionItemPage
    {
        private readonly List<ActionItemDTO> selectedItems;

        public PriorityPageActionItemContextMenuPage(List<ActionItemDTO> selectedItems)
        {
            this.selectedItems = selectedItems;
        }

        public override List<ActionItemDTO> SelectedItems
        {
            get { return selectedItems; }
        }
    }
}
