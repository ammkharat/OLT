using System.Windows.Forms;
using Com.Suncor.Olt.Common.Domain;

namespace Com.Suncor.Olt.Client.Controls
{
    public class DomainListViewItem<T> : ListViewItem where T : DomainObject
    {
        private readonly T item;

        public T Item
        {
            get { return item; }
        }

        public DomainListViewItem(T item)
        {
            this.item = item;
        }

    }

}
