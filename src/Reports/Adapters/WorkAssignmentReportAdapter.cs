using Com.Suncor.Olt.Common.Domain;

namespace Com.Suncor.Olt.Reports.Adapters
{
    public class WorkAssignmentReportAdapter
    {
        private readonly WorkAssignment workAssignment;

        public WorkAssignmentReportAdapter(WorkAssignment workAssignment)
        {
            this.workAssignment = workAssignment;
        }

        public string WorkAssignmentName
        {
            get { return workAssignment.DisplayName; }
        }
    }
}