        [Ignore]
        [Test]
        public void TimingTest()
        {
            var tags = GetTagList();

            Console.Out.WriteLine("Tag Count: " + tags.Count);

            var timeSpans = new List<TimeSpan>();

            var fastest = new TimeSpan(1, 0, 0);
            var slowest = new TimeSpan(0);

            string fastestTag = null;
            string slowestTag = null;

            var isFirstOne = true;

            foreach (var tag in tags)
            {
                var watch = new Stopwatch();
                watch.Start();

                var requestedTime = DateTime.Now;

                decimal?[] results;

                try
                {
                    results = providerWithSqlServer.FetchPHDTagValue(PlantHistorianOrigin.Test, tag,
                        new[] {requestedTime});
                }
                catch (Exception e)
                {
                    Console.WriteLine(e);
                    Console.WriteLine("Error tag: " + tag);
                    continue;
                }

                string tagValue = null;

                if (results != null && results.Length == 1 && results[0].HasValue)
                {
                    tagValue = results[0].Value.ToString(CultureInfo.InvariantCulture);
                }

                if (!isFirstOne)
                {
                    var timeSpan = watch.Elapsed;
                    timeSpans.Add(timeSpan);

                    if (timeSpan < fastest)
                    {
                        fastest = timeSpan;
                        fastestTag = tag;
                    }

                    if (timeSpan > slowest)
                    {
                        slowest = timeSpan;
                        slowestTag = tag;
                    }

                    var secondString = GetSecondDisplayString(timeSpan);
                    Console.Out.WriteLine("{0},{1},{2}", tag, secondString, tagValue);
                }

                isFirstOne = false;
            }

            long ticks = 0;

            for (var i = 1; i < timeSpans.Count; i++)
            {
                var timeSpan = timeSpans[i];
                ticks += timeSpan.Ticks;
            }

            ticks = ticks/timeSpans.Count - 1;

            var average = new TimeSpan(ticks);
            Console.Out.WriteLine("Average: " + average);
            Console.Out.WriteLine("Fastest: {0} ({1})", fastest, fastestTag);
            Console.Out.WriteLine("Slowest: {0} ({1})", slowest, slowestTag);
        }

