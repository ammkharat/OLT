        [Test][Ignore]
        public void ShouldRetrieveTagValueForDeviation()
        {
            const string tag = "P82_OILSAND_MASS";

            // Sep 1, 2010 values are approximately:
            //    0am - 1am - 10092
            //    1am - 2am -  8291
            //    2am - 3am -  6912
            // The purpose of this test is to make sure we get values for the correct hour.  
            // If this test fails because values have changed in phd, we need to revalidate
            // the values using modtag before changing them in this test.  
            {
                var fetchDeviationTagValue = providerWithSqlServer.FetchDeviationTagValue(
                    tag, new DateTime(2010, 10, 1, 5, 0, 0), new DateTime(2010, 09, 1, 1, 0, 0).AddHours(1));
                Assert.That(fetchDeviationTagValue, Is.Not.Null);
                Assert.IsTrue(fetchDeviationTagValue > 8000 && fetchDeviationTagValue < 9000,
                    "Actual: " + fetchDeviationTagValue);
            }
            {
                var fetchDeviationTagValue = providerWithSqlServer.FetchDeviationTagValue(
                    tag, new DateTime(2010, 10, 1, 5, 0, 0), new DateTime(2010, 09, 1, 2, 0, 0).AddHours(1));
                Assert.That(fetchDeviationTagValue, Is.Not.Null);
                Assert.IsTrue(fetchDeviationTagValue > 6500 && fetchDeviationTagValue < 7500,
                    "Actual: " + fetchDeviationTagValue);
            }
        }

