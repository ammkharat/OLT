using NUnit.Framework;

namespace Com.Suncor.Olt.Remote.Utilities
{
    [TestFixture]
    public class UserInformationTest
    {
        [Ignore] [Test]
        public void ShouldCapitalizeFirstLetterAndDowncaseRemainingLettersOfName()
        {
            UserInformation userInformation = new UserInformation("MARILLA", "CUTHBERT", "sapId");
            Assert.AreEqual("Marilla", userInformation.FirstName);
            Assert.AreEqual("Cuthbert", userInformation.LastName);
        }
    }
}
